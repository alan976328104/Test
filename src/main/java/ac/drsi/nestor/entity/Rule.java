package ac.drsi.nestor.entity;

public class Rule {
	private Integer id;//关联检索内容 id
	private String name; //关联检索步骤名称
	private String content;//关联检索步骤内容
	private Integer ordersort;//关联检索步骤
	private Integer relationid; //关联检索id
	private Integer projectid;//应用项目id
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Integer getOrdersort() {
		return ordersort;
	}
	public void setOrdersort(Integer ordersort) {
		this.ordersort = ordersort;
	}
	public Integer getRelationid() {
		return relationid;
	}
	public void setRelationid(Integer relationid) {
		this.relationid = relationid;
	}
	public Integer getProjectid() {
		return projectid;
	}
	public void setProjectid(Integer projectid) {
		this.projectid = projectid;
	}
	
	
}
