package ac.drsi.nestor.entity;

public class Backups {
private Integer id;//备份id
private String name;//备份名称
private String url;//备份日期
private String backupsdate;//备份状态
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getUrl() {
	return url;
}
public void setUrl(String url) {
	this.url = url;
}
public String getBackupsdate() {
	return backupsdate;
}
public void setBackupsdate(String backupsdate) {
	this.backupsdate = backupsdate;
}
public Backups() {
	super();
	// TODO Auto-generated constructor stub
}
public Backups(Integer id, String name, String url, String backupsdate) {
	super();
	this.id = id;
	this.name = name;
	this.url = url;
	this.backupsdate = backupsdate;
}


}
