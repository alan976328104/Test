package ac.drsi.nestor.entity;

public class SVDS_User {
	private Integer userId;// 用户Id
	private String id;// Id标识
	private String username;// 用户名称
	private String pwd;// 密码
	private Integer sex;// 性别
	private Integer state;// 状态
	private String code;// 用户编号
	private String cardno;// 用户工牌号
	private String keyno;// 用户key号
	private String deptname;// 部门
	private String secretlevel;// 密级
	private SVDS_Role role;// 角色对象

	/**
	 * 获取用户Id
	 * 
	 * @return
	 */
	public Integer getUserId() {
		return userId;
	}

	/**
	 * 设置用户Id
	 * 
	 * @param userId
	 *            用户Id
	 */
	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	/**
	 * 获取Id标识
	 * 
	 * @return
	 */
	public String getId() {
		return id;
	}

	/**
	 * 设置Id标识
	 * 
	 * @param id
	 *            Id标识
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * 获取用户名称
	 * 
	 * @return
	 */
	public String getUsername() {
		return username;
	}

	/**
	 * 设置用户名称
	 * 
	 * @param username
	 *            用户名称
	 */
	public void setUsername(String username) {
		this.username = username;
	}

	/**
	 * 获取密码
	 * 
	 * @return
	 */
	public String getPwd() {
		return pwd;
	}

	/**
	 * 设置密码
	 * 
	 * @param pwd
	 *            密码
	 */
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	/**
	 * 获取性别
	 * 
	 * @return
	 */
	public Integer getSex() {
		return sex;
	}

	/**
	 * 设置性别
	 * 
	 * @param sex
	 *            性别
	 */
	public void setSex(Integer sex) {
		this.sex = sex;
	}

	/**
	 * 获取状态
	 * 
	 * @return
	 */
	public Integer getState() {
		return state;
	}

	/**
	 * 设置状态
	 * 
	 * @param state
	 *            状态
	 */
	public void setState(Integer state) {
		this.state = state;
	}

	/**
	 * 获取密级
	 * 
	 * @return
	 */
	public String getSecretlevel() {
		return secretlevel;
	}

	/**
	 * 设置密级
	 * 
	 * @param secretlevel
	 *            密级
	 */
	public void setSecretlevel(String secretlevel) {
		this.secretlevel = secretlevel;
	}

	/**
	 * 角色对象
	 * 
	 * @return
	 */
	public SVDS_Role getRole() {
		return role;
	}

	/**
	 * 设置角色对象
	 * 
	 * @param role
	 *            角色对象
	 */
	public void setRole(SVDS_Role role) {
		this.role = role;
	}

	/**
	 * 获取用户编号
	 * 
	 * @return
	 */
	public String getCode() {
		return code;
	}

	/**
	 * 设置用户编号
	 * 
	 * @param code
	 *            用户编号
	 */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * 获取用户工牌号
	 * 
	 * @return
	 */
	public String getCardno() {
		return cardno;
	}

	/**
	 * 设置用户工牌号
	 * 
	 * @param cardno
	 *            用户工牌号
	 */
	public void setCardno(String cardno) {
		this.cardno = cardno;
	}

	/**
	 * 获取用户key号
	 * 
	 * @return
	 */
	public String getKeyno() {
		return keyno;
	}

	/**
	 * 设置用户key号
	 * 
	 * @param keyno
	 *            用户key号
	 */
	public void setKeyno(String keyno) {
		this.keyno = keyno;
	}

	/**
	 * 获取部门名称
	 * 
	 * @return
	 */
	public String getDeptname() {
		return deptname;
	}

	/**
	 * 设置部门名称
	 * 
	 * @param deptname
	 *            部门名称
	 */
	public void setDeptname(String deptname) {
		this.deptname = deptname;
	}


	@Override
	public String toString() {
		return "SVDS_User [userId=" + userId + ", id=" + id + ", username="
				+ username + ", pwd=" + pwd + ", sex=" + sex + ", state="
				+ state + ", code=" + code + ", cardno=" + cardno + ", keyno="
				+ keyno + ", deptname=" + deptname + ", secretlevel="
				+ secretlevel + ", role=" + role + "]";
	}

}
